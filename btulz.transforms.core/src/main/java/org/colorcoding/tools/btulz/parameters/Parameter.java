package org.colorcoding.tools.btulz.parameters;

/**
 * 参数
 * 
 * @author Niuren.Zhu
 *
 */
public class Parameter {
	private int id;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	private String name;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	private Object value;

	public Object getValue() {
		return value;
	}

	public void setValue(Object value) {
		this.value = value;
	}
}
